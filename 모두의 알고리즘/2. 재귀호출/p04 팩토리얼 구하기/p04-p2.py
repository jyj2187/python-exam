#최댓값 구하기 2
#재귀호출 이용
#리스트 a와 리스트의 갯수 len_a를 입력받는다.
#리스트의 맨 마지막 숫자부터 앞으로 한자리씩 재귀호출을 이용하여 첫번쨰(0번)자리까지 비교한다.
#더 큰 숫자를 재귀호출로 연속적으로 비교한다.

def find_max(a, len_a):
    if len_a == 1:
        return a[0]
    max_n_1 = find_max(a, len_a - 1)
    if max_n_1 > a[len_a - 1]:
        return max_n_1
    else:
        return a[len_a - 1]

v = [17, 92, 18, 33, 58, 7, 33, 42]
print(find_max(v, len(v)))


























































































































































